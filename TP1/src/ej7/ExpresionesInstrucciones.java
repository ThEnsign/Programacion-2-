package ej7;

public class ExpresionesInstrucciones {
    public static void main(String[] args) {
        int x = 10;          // declaracion + asignacion (instruccion)
        x = x + 5;           // asignacion (instruccion) que usa la expresion x + 5
        System.out.println(x); // llamada (instruccion) que evalua la expresion x
    }
}
